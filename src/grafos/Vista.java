    /**
     * @param Ver
     * @param args the command line arguments
     */
    public void Cero(int Ver) {
        for (int i = 0; i < Ver; i++) {
            for (int j = 0; j < Ver; j++) {
                Adjacencia[i][j] = 0;
            }
        }
    }

